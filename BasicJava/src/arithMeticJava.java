

public class arithMeticJava {
    public static void main(String[] args) {
        int a = 20;
        int b = 6;

        // Addition
        int sum = a + b;
        System.out.println("Addition (a + b): " + sum);

        // Subtraction
        int diff = a - b;
        System.out.println("Subtraction (a - b): " + diff);

        // Multiplication
        int product = a * b;
        System.out.println("Multiplication (a * b): " + product);

        // Division
        int quotient = a / b;
        System.out.println("Division (a / b): " + quotient);

        // Modulus
        int remainder = a % b;
        System.out.println("Modulus (a % b): " + remainder);

        // Float division
        float fQuotient = (float) a / b;
        System.out.println("Float Division (a / b): " + fQuotient);
    }
}
